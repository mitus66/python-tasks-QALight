'''
Написати модуль який буде шукати і друкувати повний рядок з файлу незалежно від того, що користувач передав код, абревіатуру або повну назву елемента
elements.txt elements.txt  12 березня 2024, 07:52 AM
'''

def search_element():
    # Відкриваємо файл для читання
    with open('elements.txt', 'r') as file:
        # Зчитуємо всі рядки з файлу
        lines = file.readlines()

    # Запитуємо в користувача введення
    user_input = input("Введіть номер елемента, його скорочену або повну назву: ")

    # Перебираємо всі рядки з файлу
    for line in lines:
        # Розділяємо рядок на елементи
        elements = line.split(',')
        # Якщо введення користувача знайдено в рядку як окремий елемент
        if user_input.strip() in (element.strip() for element in elements):
            # Друкуємо повний рядок
            print(line)

# Викликаємо функцію
if __name__ == '__main__':
    search_element()



